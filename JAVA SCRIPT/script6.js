// giving different data types to variables
//typeof - use to tell type of data used

///string
let str_demo = "this is a string";
console.log('string:',str_demo);
console.log('string:', typeof str_demo);
//number
let num_demo = 25;
console.log('number:',num_demo);
console.log('number:',typeof num_demo);

//float
let float_demo = 5.3;
console.log('floating num:',float_demo);
console.log('floating num:',typeof float_demo);

//booleans
let bool_demo = true;
console.log('boolean:',bool_demo);
console.log('boolean:',typeof bool_demo);

//null
let null_demo = null;
console.log('null:',null_demo);
console.log('null:',typeof null_demo);

//undefined
let undef_demo = undefined;
console.log('undefined:',undef_demo);
console.log('undefined:',typeof undef_demo);

let undef_demo2;
console.log('undefined:',undef_demo2);
console.log('undefined:',typeof undef_demo2);

//objects
const obj_demo = { width: 250, height: 250, color: "red" };
console.log('object:',obj_demo);
console.log('object:',typeof obj_demo);

//arrays
const arr_demo = ['a','b','c'];
console.log('array:',arr_demo);
console.log('array:',typeof arr_demo);

